    private void loadBills() {
        try {
            List<Bill> bills = billController.getAllBillsForTable();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Clear existing rows

            for (Bill bill : bills) {
                Object[] row = new Object[]{
                        bill.getBillNumber(),
                        bill.getCustomerName(),
                        bill.getPhone(),
                        bill.getEmail(),
                        "N/A", // createdAt placeholder; replace with bill.getCreatedAt() when available
                        bill.getGrandTotal(),
                        bill.getCreatedBy()
                };
                model.addRow(row);
            }

        } catch (Exception e) {
            logger.severe(e.toString());
            e.printStackTrace();
        }
    }

