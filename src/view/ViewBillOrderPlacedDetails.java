    private void FilterByDateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FilterByDateActionPerformed
        // TODO add your handling code here:
        try {
        String dateFilter = FilterByDate.getText().trim(); // Expected format: "yyyy-MM-dd"
        if (dateFilter.isEmpty()) {
            loadBills(); // Load all bills if no date is entered
            return;
        }

        List<Bill> bills = billController.getAllBillsForTable(); // Fetch all bills
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear table

        for (Bill bill : bills) {
            // Assuming you have a getCreatedAt() method returning a String "yyyy-MM-dd"
            // If not, replace this with proper date handling from your database
            String billDate = "N/A"; // replace with bill.getCreatedAt() if available
            if (billDate.equals(dateFilter)) {
                Object[] row = new Object[]{
                        bill.getBillNumber(),
                        bill.getCustomerName(),
                        bill.getPhone(),
                        bill.getEmail(),
                        billDate,
                        bill.getGrandTotal(),
                        bill.getCreatedBy()
                };
                model.addRow(row);
            }
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_FilterByDateActionPerformed

